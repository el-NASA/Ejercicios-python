def binario(numero):
    a = format(numero, "b")
    return a


def suma(num1, num2):
    return format((int(num1, 2) + int(num2, 2)), "b")


def resta(num1, num2):
    return format((int(num1, 2) - int(num2, 2)), "b")


def mult(num1, num2):
    return format((int(num1, 2) * int(num2, 2)), "b")


def div(num1, num2):
    return format((int(num1, 2) - int(num2, 2)), "b")
